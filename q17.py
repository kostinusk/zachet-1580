# Граф - это совокупность вершин (также известных как узлы) и ребер, соединяющих пары вершин. Графы широко используются в информатике и других областях для моделирования взаимосвязей между объектами или сущностями.

# Существует два основных типа графов: ориентированные графы и неориентированные графы. В ориентированном графе каждое ребро имеет направление, что означает, что существует "исходная" вершина и "конечная" вершина. В неориентированном графе ребра не имеют направления и могут быть пересечены в любом направлении.

# Графики могут быть представлены несколькими способами, в том числе:

    # Матрица смежности: квадратная матрица, в которой строки и столбцы представляют вершины, а записи указывают, есть ли ребро между вершинами. Например, 1 в записи (i, j) матрицы означает, что существует ребро от вершины i до вершины j.

    # Список смежности: список списков, где каждый элемент является списком вершин, смежных с определенной вершиной. Например, если i-й элемент списка содержит [j, k], это означает, что существуют ребра от вершины i до вершин j и k.

    # Список ребер: список кортежей, где каждый кортеж представляет ребро между двумя вершинами. Например, кортеж из (i, j) представляет ребро от вершины i до вершины j.

# Часто существует возможность преобразования между этими представлениями. Например, матрица смежности может быть преобразована в список смежности путем перебора строк матрицы и создания списка смежных вершин для каждой вершины. Аналогично, список смежности можно преобразовать в список ребер, выполнив итерацию по списку и создав кортеж для каждого ребра.

# Вот пример функции Python, которая преобразует матрицу смежности в список смежностей:

def matrix_to_list(matrix):
    n = len(matrix)
    adj_list = [[] for _ in range(n)]
    for i in range(n):
        for j in range(n):
            if matrix[i][j] == 1:
                adj_list[i].append(j)
    return adj_list
  
# Параметр matrix представляет собой квадратную матрицу, представляющую график, и функция создает пустой список смежности того же размера. Затем он выполняет итерацию по матрице и для каждой ненулевой записи добавляет соответствующую вершину в список соседних вершин для этой вершины. Наконец, функция возвращает список смежности.

